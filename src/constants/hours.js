export const createTimeDuration = ({
  messages,
  minDuration = 15,
  hoursDuration = 12,
  startFrom = 1,
}) => {
  const newArrayOfTimes = [];

  for (let i = startFrom; i < (60 / minDuration) * hoursDuration + 1; i++) {
    newArrayOfTimes.push({
      key: i,
      text: convertMinsToHours(i * minDuration, messages),
      id: createTimesIds(i * minDuration),
      timeInMin: i * minDuration,
    });
  }
  return newArrayOfTimes;
};

const convertMinsToHours = (mins, messages) => {
  const hrsNum = Math.floor(mins / 60);
  const minsNum = mins % 60;
  if (hrsNum === 0 && minsNum === 0) {
    return `0 ${messages['common.minutes']}`;
  }
  return `${hrsNum === 0 ? `` : `${hrsNum} ${messages['common.hours']} `}${
    minsNum === 0 ? `` : `${minsNum} ${messages['common.minutes']}`
  }`;
};

export const createTimesIds = (mins) => {
  const hrsNum = Math.floor(mins / 60);
  const minsNum = mins % 60;
  return `${hrsNum <= 9 ? `0${hrsNum}` : `${hrsNum}`}:${
    minsNum < 10 ? `0${minsNum}` : minsNum
  }:00`;
};

export const convertTimeToNum = (time) => {
  if (time) {
    const timeArr = time.split(':');
    const num = +timeArr[0] * 60 + +timeArr[1];
    return num;
  }
  return 0;
};
